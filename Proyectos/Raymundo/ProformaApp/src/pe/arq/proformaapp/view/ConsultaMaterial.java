    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        try {
            //Datos
            String cuenta = txtCodigo.getText();
            //Proceso
            MaterialController control = new MaterialController();
            List<Map<String, ?>> lista = control.getMovimientos(cuenta);
            //Mostrar
            DefaultTableModel tabla;
            tabla = (DefaultTableModel) tblResultado.getModel();
            tabla.setRowCount(0);
            for (Map<String, ?> r : lista) {
                Object[] rowData = {
                r.get("idMaterial").toString(),
                r.get("nombre").toString(),
                r.get("unidad").toString()
                };
                tabla.addRow(rowData);
            }
        } catch (Exception e) {
            Mensaje.error(this, e.getMessage());
        }
    }//GEN-LAST:event_btnConsultarActionPerformed


